    private void botonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActionPerformed
        // TODO add your handling code here:
        boton.cambiarEstado();
        
        List<Ficha> fichas = new ArrayList<>();
        fichas.add(ficha1);
        fichas.add(ficha2);
        fichas.add(ficha3);
        fichas.add(ficha4);
        fichas.add(ficha5);
        fichas.add(ficha6);
        fichas.add(ficha7);
        fichas.add(ficha8);
        fichas.add(ficha9);
        
        if (boton.isEstado()) {
            for (Ficha f : fichas) {
                f.setEnabled(true);
                f.mostrarNumero();
            }
        }
        
        if (!boton.isEstado()) {
            for (Ficha f : fichas) {
                f.reset();
                f.ocultarNumero();
            }
        }
    }//GEN-LAST:event_botonActionPerformed

